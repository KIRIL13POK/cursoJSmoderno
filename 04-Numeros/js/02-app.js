'use strict';

const numero1 = 30;
const numero2 = 20;

let resultado;

//Suma:
resultado = numero1 + numero2;
//Resta:
resultado = numero1 - numero2;
//Multiplicación:
resultado = numero1 * numero2;
//División:
resultado = numero1 / numero2;
//Modulo:El operador resto (%) devuelve el resto de la división entre dos operandos. Siempre toma el signo del dividendo.
resultado = numero1 % numero2;

console.log(resultado);
